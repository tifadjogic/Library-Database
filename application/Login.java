    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
    	
      Connection con=null;
		con= OracleCon.connectOracle();
		try {
			String sql="select* from member_table where member_id=? and user_password=?";
			pst= (OraclePreparedStatement) con.prepareStatement(sql);
			pst.setString(1, jTextField1.getText());
			pst.setString(2, jTextField2.getText());
			rs=(OracleResultSet) pst.executeQuery();
              if(rs.next())
              {
	                int result = rs.getInt("member_id"); 
	                String role=rs.getString("role");
	                if(role.equals("user")) { //If boolean value 1
	                    new userMenu(result).setVisible(true); //redirect to user menu
	                    setVisible(false);
	                }
	                else if(role.equals("librarian")){
	                	new adminMenu(result).setVisible(true); //redirect to admin menu
	                    setVisible(false);
	                }
	          
              }
	                else
	                    JOptionPane.showMessageDialog(null,"Wrong Username/Password!"); //Display Message
	                	con.close();   
	                	pst.close();
          }
		
		
		catch(Exception e){
			JOptionPane.showMessageDialog(null,"Wrong Username/Password!"); //Display Message
		}
		
    }
